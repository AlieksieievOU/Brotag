import type { Member } from "../store/types.js";

const ROASTS = [
  "{name}, ти як WI-FI у метро — всі знають, що ти є, але ніхто не розраховує на стабільність.",
  "{name} обіцяв(-ла) прийти на 5 хвилин раніше. Наразі запізнення триває третій рік.",
  "{name} відповідає на повідомлення зі швидкістю льодовика, який передумав танути.",
  "У {name} стільки нечитаних сповіщень, що ними можна опалювати квартиру взимку.",
  "{name} — живий доказ того, що дедлайн це лише пропозиція.",
  "{name} говорить \"я на хвилинку\" з такою впевненістю, ніби це колись було правдою.",
  "Якби прокрастинація була олімпійським видом спорту, {name} вже мав(-ла) би золото.",
  "{name} читає повідомлення, ставить дві синіх галочки і зникає, як укрзалізниця за розкладом.",
  "{name} завжди має план. Просто цей план ніколи не переходить у дію.",
  "{name}, твоя пунктуальність — як єдиноріг: всі про неї чули, ніхто не бачив.",
];

export function handleRoast(members: Member[], target: Member | undefined, rng: () => number = Math.random): string {
  const victim = target ?? (members.length > 0 ? members[Math.floor(rng() * members.length)] : undefined);
  if (!victim) return "Бот поки що нікого тут не знає, щоб підколоти. Напишіть щось у чат спочатку.";

  const template = ROASTS[Math.floor(rng() * ROASTS.length)];
  return `🔥 ${template.replace("{name}", victim.firstName)}`;
}
